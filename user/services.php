<h3>	Tatkal Booking</h3>
<p>Under the Tatkal scheme, passengers who plan their journey at short notice can book their tickets in almost all Mail/Express trains through the Indian railways internet portal. The booking starts at 10:00 AM daily, one day prior to the departure of the train from source station. It is recommended to use the Chrome browser instead of Firefox or IE for successful booking of Tatkal tickets. </p>
