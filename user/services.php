	<h3>Online ticketing</h3>
<p>IRCTC is better known for changing the face of railway ticketing in India. It pioneered internet-based rail ticket booking through its website, as well as from the mobile phones via GPRS or SMS. Ticket cancellations can also be done online. In addition to e-tickets, IRCTC also offers I-tickets that are basically like regular tickets except that they are booked online and delivered by post. The tickets PNR status is also made available. Commuters on the Mumbai Suburban Railway can also book season tickets through the IRCTC website. IRCTC has also launched a loyalty program called Shubh Yatra for frequent travelers. Through this program, passengers can avail of discounts on all tickets booked round the year by paying an upfront annual fee. </p>
<p>Seeking to make it easier to book e-tickets, IRCTC launched a scheme called Rolling Deposit Scheme (RDS). RDS is a hassle-free e-ticket booking scheme allowing passengers to reserve seats against advance money kept with the corporation. IRCTC has also added flights and hotels booking facilities to their line of online reservation services. 
Generally there is a huge load on the website at 10am, when booking of Tatkal tickets opens. The booking of tickets varies from time to time based on festivals and vacations. IRCTC is currently using 58 iridium servers and 10 more servers to be added to cater the heavy traffic in peak hours. One of the technologies used in building the website is struts2 which is related to java language. </p>
<p>As of May 2013, the ticketing site of the IRCTC is able to handle about 1.2 lakh concurrent connections on web servers and can book about 2000 tickets per minute. Indian Railways plans to spend about  100 crore (US$17 million) to strengthen the website to enable it for booking 7200 tickets per minute. CRIS, the technical arm of railways, is involved in the upgrading of the IRCTC website. According to a senior Railway Ministry official, the average booking per day has been increased from 3.67 lakh in 2012 to 4.15 lakh in May 2013. Approximately, 31 crore reserve tickets are booked in a year out of which 55% of tickets are sold through windows, 37% of tickets are booked online and 8% are booked by ticketing agents.</p> 
